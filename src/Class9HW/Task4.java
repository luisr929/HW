package Class9HW;

public class Task4 { public static void main(String[] args) {
    String[] cars = {"bmw", "mercedes", "jeep", "infiniti", "ford", "cadillac"};
    for (int i = 0; i < cars.length; i++) {
        System.out.print(cars[i] + " ");

    }
    int j = 0;
    while (j < cars.length) {
        System.out.println(cars[j] + " ");
        j++;

    }
}
}
